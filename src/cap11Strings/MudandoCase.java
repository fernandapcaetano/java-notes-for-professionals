package cap11Strings;

public class MudandoCase {
    public static void main(String[] args) {
        String qualquer = "Escrevendo Qualquer Coisa";
        String caixaAlta = qualquer.toUpperCase();
        String caixaBaixa = qualquer.toLowerCase();

        System.out.println(qualquer);
        System.out.println(caixaAlta);
        System.out.println(caixaBaixa);
    }
}
